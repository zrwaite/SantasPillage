import Block from "./block.js"
import Door from "./door.js"
import Person from "./person.js"
import Elf from "./elf.js"
import Ginger from "./ginger.js"

export function onePBuild(game, level) {
  let playeri = 1
  let blocki = 1
  let doori = 1
  let elfi = 1
  let gingeri = 1
  let blocks = [[],[],[],[],[]]
  let levelLen = 0
  level.forEach((row, rowIndex) => {
    row.forEach((block, blockIndex) => {
      if (block === 1) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        if (playeri < 2){
          blocks[0].push(new Person(game, playeri, pos))
        }
        playeri++
      } else if (block === 2) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[1].push(new Block(game, blocki, pos))
        blocki++
      } else if (block === 3) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[2].push(new Door(game, doori, pos))
        doori++
      } else if (block === 4) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[3].push(new Elf(game, elfi, pos))
        elfi++
      } else if (block === 5) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[4].push(new Ginger(game, elfi, pos))
        gingeri++
      }
    })
    levelLen = row.length*game.block.width
  })
  blocks.push(levelLen)
  return blocks
}
