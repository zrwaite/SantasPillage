import Block from "./block.js"
import Door from "./door.js"
import Person from "./person.js"
import Elf from "./elf.js"
import Ginger from "./ginger.js"
import Cane from "./cane.js"
import Icicle from "./icicle.js"
import Snowman from "./snowman.js"
import Deer from "./deer.js"
import Cookie from "./cookie.js"

export function build(game, level) {
  var pos = null
  let playeri = 1
  let blocki = 1
  let doori = 1
  let elfi = 1
  let gingeri = 1
  let canei = 1
  let iciclei=1
  let snowmani=1
  let deeri=1
  let cookiei=1
  let blocks = [[],[],[],[],[],[],[],[],[],[]]
  let levelLen = 0
  level.forEach((row, rowIndex) => {
    row.forEach((block, blockIndex) => {
      if (block!==0){
        pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        switch(block){
          case 1:
            if (playeri < game.numPlayers+1){blocks[0].push(new Person(game.players[playeri-1], game.info, playeri, pos))}
            playeri++
            break
          case 2:
            let covered = false
            if(rowIndex>0){
              if(level[rowIndex-1][blockIndex]===2){
                covered = true
              }
            }
            blocks[1].push(new Block(game.info, blocki, pos, covered))
            blocki++
            break
          case 3:
            blocks[2].push(new Door(game.info, doori, pos))
            doori++
            break
          case 4:
            blocks[3].push(new Elf(game.info, elfi, pos))
            elfi++
            break
          case 5:
            blocks[4].push(new Ginger(game.info, elfi, pos))
            gingeri++
            break
          case 6:
            blocks[5].push(new Cane(pos))
            canei++
            break
          case 7:
            blocks[6].push(new Icicle(game.info, pos))
            iciclei++
            break
          case 8:
            blocks[7].push(new Snowman(game.info, snowmani, pos))
            snowmani++
            break
          case 9:
            blocks[8].push(new Deer(game.info, deeri, pos))
            deeri++
            break
          case 10:
            blocks[9].push(new Cookie(game.info, cookiei, pos))
            cookiei++
            break
        }
      }
    })
    levelLen = row.length*game.block.width
  })
  blocks.push(levelLen)
  return blocks
}
